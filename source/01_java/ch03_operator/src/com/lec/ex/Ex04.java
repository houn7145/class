package com.lec.ex;
// 논리연산자 : %%(AND 둘다  true), ||(OR 하나만 true), !(반대)
public class Ex04 {
	public static void main(String[] args) {
		int i=1, j=10, h=10;
		System.out.println("&& : (i<j) && (++j>h)는 " + ((i<j) && (++j>h)) );
		System.out.println("j = " +j); // j=11
		
		System.out.println("&& : (i>j) && (++j>h)는 " + ((i>j) && (++j>h)) );
		System.out.println("j = " +j);
		//앞이 f이기에 뒤는 계산 x 
		//&&연산위 경우 좌항이 false의 경우 우항의 값은 계산하지 않음
		
		System.out.println("|| : (i<j) || (++j>h)는 " + ((i<j) || (++j>h)) );
		System.out.println("j = " +j); // j=11
		
		System.out.println("|| : (i>j) || (++j>h)는 " + ((i>j) || (++j>h)) );
		System.out.println("j = " +j);
		//앞이 t일 경우에 뒤에는 계산 x
		//앞이 f일 경우에 뒤를 계산
	}

}
